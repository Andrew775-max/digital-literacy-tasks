#include <iostream>        // Подключаем библиотеку для ввода/вывода
using namespace std;       // Используем стандартное пространство имен (чтобы не писать std::)

int main() {               // Главная функция программы
    setlocale(LC_ALL, "RU");  // Включаем русскую локаль для отображения кириллицы
    
    int n;                 // Объявляем переменную для хранения трехзначного числа
    
    // Запрашиваем у пользователя ввод числа
    cout << "Введите трехзначное число: ";
    cin >> n;              // Считываем введенное число в переменную n
    
    // Разбиваем трехзначное число на цифры:
    int first = n / 100;       // Первая цифра: делим на 100 (123 / 100 = 1)
    int middle = (n / 10) % 10; // Вторая цифра: делим на 10, затем берем остаток от деления на 10
                               // (123 / 10 = 12, 12 % 10 = 2)
    int last = n % 10;        // Третья цифра: остаток от деления на 10 (123 % 10 = 3)
    
    // Меняем местами первую и последнюю цифры:
    // last*100 + middle*10 + first = 3*100 + 2*10 + 1 = 321
    int res = last * 100 + middle * 10 + first;
    
    // Делаем число четырехзначным, добавляя ноль в конец:
    // 321 * 10 = 3210
    int new_res = res * 10;
    
    // Разбиваем четырехзначное число на цифры:
    int c1 = new_res / 1000;      // Первая цифра: 3210 / 1000 = 3
    int c2 = (new_res / 100) % 10; // Вторая цифра: (3210/100)=32, 32%10=2
    int c3 = (new_res / 10) % 10;  // Третья цифра: (3210/10)=321, 321%10=1
    int c4 = new_res % 10;         // Четвертая цифра: 3210 % 10 = 0
    
    // Меняем местами вторую и третью цифры в четырехзначном числе:
    // c1*1000 + c3*100 + c2*10 + c4 = 3*1000 + 1*100 + 2*10 + 0 = 3120
    int result = c1 * 1000 + c3 * 100 + c2 * 10 + c4;
    
    // Выводим результат на экран
    cout << "Результат: " << result << endl;
    
    return 0;  // Завершаем программу с кодом 0 (успешное выполнение)
}
